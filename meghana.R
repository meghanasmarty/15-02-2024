  Task-1:  Sort elements by their frequency and index
Given an integer array, sort its element by their frequency and index. i.e., if two elements have different frequencies, then the one which has more frequency should come first; otherwise, the one which has less index should come first.
For example,    Input : [3, 3, 1, 1, 1, 8, 3, 6, 8, 7, 8]        Output: [3, 3, 3, 1, 1, 1, 8, 8, 8, 6, 7]

Task-2:  In-place merge two sorted arrays
Given two sorted arrays, X[] and Y[] of size m and n each, merge elements of X[] with elements of array Y[] by maintaining the sorted order, i.e., fill X[] with the first m smallest elements and fill Y[] with remaining elements.
Do the conversion in-place and without using any other data structure.
For example,   
Input:   X[] = { 1, 4, 7, 8, 10 }    Y[] = { 2, 3, 9 } 
Output: X[] = { 1, 2, 3, 4, 7 }Y[] = { 8, 9, 10 }

